    private void cambiarVelocidad(){
        if(jCheckBoxRapido.isSelected()){
                velocidad=0;
            }else{
                velocidad=100;
            }
         if(sim!=null){
            if(jCheckBoxRapido.isSelected()){
                sim.setVelocidad(0);
            }else{
                sim.setVelocidad(100);
            }
        }
    }

